package week1.day1;

public class FindOddNumbers 
{

	public static void main(String[] args) 
	{		
		int maxRange=10;  
		System.out.print("List of odd numbers : ");  
		for (int i=1; i<=maxRange; i++)   
		{  		
			if (i%2==1)   
			{  
				System.out.print(i + " ");  
			}	
		}	
	}  
		

}

